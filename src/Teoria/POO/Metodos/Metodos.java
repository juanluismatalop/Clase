package Teoria.POO.Metodos;

public class Metodos {
    public static int sumarDosValores (int uno, int dos){
        return uno + dos;
    }
    public static int sumarValores(int uno){
        return uno;
    }
    public static int sumarDosValores (double uno, double dos){
        return (int) (uno + dos);
    }
}
